def findBestShift(wordList, text):
    """
    Finds a shift key that can decrypt the encoded text.

    text: string
    returns: 0 <= int < 26
    """
    max_real_words = 0
    best_shift = 0
    for i in range(26):
        word_list = applyShift(text, i).split(" ")
        temp_n_words = 0
        for word in word_list:
            if isWord(wordList, word):
                temp_n_words += 1
        if temp_n_words > max_real_words:
            max_real_words = temp_n_words
            best_shift = i
    return best_shift
    

